#!/bin/sh

ZEXY_H=z_zexy.h
ZEXY_C=z_zexy.c
ZEXY_S=Make.source

function head_h() {
 echo "/* zexy-setup autogenerated header-file"
 echo " * generated by \"$0\""
 echo " * !! DO NOT MANUALLY EDIT  !!"
 echo " */"
 echo
 echo "#ifndef Z_ZEXY_H__"
 echo "#define Z_ZEXY_H__"
}

function foot_h() {
 echo "#endif /* Z_ZEXY_H__ */"
 echo ""
}

function head_c() {
 echo "/* zexy-setup autogenerated setup-file"
 echo " * generated by \"$0\""
 echo " * !! DO NOT MANUALLY EDIT  !!"
 echo " */"
 echo 
 echo "#include \"$ZEXY_H\""
 echo
 echo "void z_zexy_setup(void)"
 echo "{"
}

function foot_c() {
 echo "}"
 echo
}

function head_s() {
  echo "SOURCES = \\"
}

function foot_s() {
  echo "	zexy.c"
  echo
}

head_h > $ZEXY_H
head_c > $ZEXY_C
head_s > $ZEXY_S

for i in `ls *.c | grep -v zexy.c`
do
  NAME="${i%.c}"
  SETUPNAME="void ${NAME}_setup(void)"
  if grep "$SETUPNAME" $i > /dev/null
  then
#    echo ${i%.c}
    echo "	$i \\" >> $ZEXY_S
    echo "$SETUPNAME;" >> $ZEXY_H
    echo "	${NAME}_setup();" >> $ZEXY_C
  fi
done

foot_h >> $ZEXY_H
foot_c >> $ZEXY_C
foot_s >> $ZEXY_S

